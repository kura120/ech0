#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn node_round_trips_through_json() {
        let node = Node {
            id: Uuid::new_v4(),
            kind: "person".to_string(),
            metadata: serde_json::json!({"name": "Alice", "age": 30}),
            importance: 0.8,
            created_at: Utc::now(),
            ingest_id: Uuid::new_v4(),
            source_text: Some("Alice is 30 years old.".to_string()),
        };

        let serialized = serde_json::to_string(&node).expect("node should serialize to JSON");
        let deserialized: Node =
            serde_json::from_str(&serialized).expect("JSON should deserialize back to Node");

        assert_eq!(deserialized.id, node.id);
        assert_eq!(deserialized.kind, node.kind);
        assert_eq!(deserialized.metadata, node.metadata);
        assert!((deserialized.importance - node.importance).abs() < f32::EPSILON);
        assert_eq!(deserialized.ingest_id, node.ingest_id);
        assert_eq!(deserialized.source_text, node.source_text);
    }

    #[test]
    fn edge_round_trips_through_json() {
        let edge = Edge {
            source: Uuid::new_v4(),
            target: Uuid::new_v4(),
            relation: "knows".to_string(),
            metadata: serde_json::json!({"since": "2024"}),
            importance: 0.6,
            created_at: Utc::now(),
            ingest_id: Uuid::new_v4(),
        };

        let serialized = serde_json::to_string(&edge).expect("edge should serialize to JSON");
        let deserialized: Edge =
            serde_json::from_str(&serialized).expect("JSON should deserialize back to Edge");

        assert_eq!(deserialized.source, edge.source);
        assert_eq!(deserialized.target, edge.target);
        assert_eq!(deserialized.relation, edge.relation);
    }

    #[test]
    fn node_round_trips_through_msgpack() {
        let node = Node {
            id: Uuid::new_v4(),
            kind: "fact".to_string(),
            metadata: serde_json::json!({"statement": "the sky is blue"}),
            importance: 1.0,
            created_at: Utc::now(),
            ingest_id: Uuid::new_v4(),
            source_text: None,
        };

        let bytes = rmp_serde::to_vec(&node).expect("node should serialize to msgpack");
        let deserialized: Node =
            rmp_serde::from_slice(&bytes).expect("msgpack should deserialize back to Node");

        assert_eq!(deserialized.id, node.id);
        assert_eq!(deserialized.kind, node.kind);
    }

    #[test]
    fn edge_round_trips_through_msgpack() {
        let edge = Edge {
            source: Uuid::new_v4(),
            target: Uuid::new_v4(),
            relation: "occurred_at".to_string(),
            metadata: serde_json::json!({}),
            importance: 0.5,
            created_at: Utc::now(),
            ingest_id: Uuid::new_v4(),
        };

        let bytes = rmp_serde::to_vec(&edge).expect("edge should serialize to msgpack");
        let deserialized: Edge =
            rmp_serde::from_slice(&bytes).expect("msgpack should deserialize back to Edge");

        assert_eq!(deserialized.source, edge.source);
        assert_eq!(deserialized.target, edge.target);
        assert_eq!(deserialized.relation, edge.relation);
    }

    #[test]
    fn memory_tier_equality() {
        assert_eq!(MemoryTier::ShortTerm, MemoryTier::ShortTerm);
        assert_ne!(MemoryTier::ShortTerm, MemoryTier::Episodic);
        assert_ne!(MemoryTier::Episodic, MemoryTier::Semantic);
    }

    #[test]
    fn search_options_default_is_balanced() {
        let options = SearchOptions::default();
        assert_eq!(options.limit, 10);
        assert!((options.vector_weight - 0.5).abs() < f32::EPSILON);
        assert!((options.graph_weight - 0.5).abs() < f32::EPSILON);
        assert!((options.min_importance - 0.0).abs() < f32::EPSILON);
        assert!(options.tiers.is_empty(), "default should search all tiers");
    }

    #[test]
    fn traversal_options_default() {
        let options = TraversalOptions::default();
        assert_eq!(options.max_depth, 3);
        assert_eq!(options.limit, 50);
        assert!((options.min_importance - 0.0).abs() < f32::EPSILON);
        assert!(options.relation_filter.is_none());
    }

    #[test]
    fn retrieval_source_variants() {
        assert_ne!(RetrievalSource::Vector, RetrievalSource::Graph);
        assert_ne!(RetrievalSource::Graph, RetrievalSource::Both);
        assert_eq!(RetrievalSource::Vector, RetrievalSource::Vector);
    }

    #[test]
    fn ingest_result_debug_does_not_panic() {
        let result = IngestResult {
            ingest_id: Uuid::new_v4(),
            nodes_written: 3,
            edges_written: 2,
            #[cfg(feature = "contradiction-detection")]
            conflicts: Vec::new(),
            #[cfg(all(feature = "dynamic-linking", feature = "tokio"))]
            linking_task: None,
        };
        // Verify Debug impl works without panicking
        let debug_output = format!("{result:?}");
        assert!(debug_output.contains("IngestResult"));
        assert!(debug_output.contains("nodes_written"));
    }
}
